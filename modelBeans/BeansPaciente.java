package modelBeans;

/**
 *
 * @author daniel
 */
public class BeansPaciente {
    private int codPac;
    private String nomePac;
    private String telefonePac;
    private String cpfPac;
    private String nas;
    private String cormobidade;
    private String pesquisa;
    private String residencia;

    public int getCodPac() {
        return codPac;
    }

    public void setCodPac(int codPac) {
        this.codPac = codPac;
    }

    public String getNomePac() {
        return nomePac;
    }

    public void setNomePac(String nomePac) {
        this.nomePac = nomePac;
    }

    public String getTelefonePac() {
        return telefonePac;
    }

    public void setTelefonePac(String telefonePac) {
        this.telefonePac = telefonePac;
    }

    public String getCpfPac() {
        return cpfPac;
    }

    public void setCpfPac(String cpfPac) {
        this.cpfPac = cpfPac;
    }

    public String getNas() {
        return nas;
    }

    public void setNas(String nas) {
        this.nas = nas;
    }

    public String getCormobidade() {
        return cormobidade;
    }

    public void setCormobidade(String cormobidade) {
        this.cormobidade = cormobidade;
    }

    public String getPesquisa() {
        return pesquisa;
    }

    public void setPesquisa(String pesquisa) {
        this.pesquisa = pesquisa;
    }

    public String getResidencia() {
        return residencia;
    }

    public void setResidencia(String residencia) {
        this.residencia = residencia;
    }
}